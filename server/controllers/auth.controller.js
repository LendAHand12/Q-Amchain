import User from "../models/User.model.js";
import Transaction from "../models/Transaction.model.js";
import bcrypt from "bcryptjs";
import jwt from "jsonwebtoken";
import crypto from "crypto";
import { sendVerificationEmail } from "../utils/email.service.js";
import { generateSecret, verifyToken, generateQRCode } from "../utils/twoFactor.service.js";

// Generate JWT tokens for user
const generateTokens = (userId) => {
  const accessToken = jwt.sign({ userId, type: "user" }, process.env.JWT_SECRET, {
    expiresIn: process.env.JWT_EXPIRE || "1d",
  });
  const refreshToken = jwt.sign({ userId, type: "user" }, process.env.JWT_REFRESH_SECRET, {
    expiresIn: process.env.JWT_REFRESH_EXPIRE || "7d",
  });
  return { accessToken, refreshToken };
};

export const register = async (req, res) => {
  try {
    const {
      email,
      username,
      password,
      referrerCode,
      walletAddress,
      fullName,
      phoneNumber,
      identityNumber,
    } = req.body;

    // Check if user exists
    const existingUser = await User.findOne({
      $or: [{ email }, { username }],
    });

    if (existingUser) {
      return res.status(400).json({
        success: false,
        message: "Email or username already exists",
      });
    }

    // Validate referrerCode is provided
    if (!referrerCode || !referrerCode.trim()) {
      return res.status(400).json({
        success: false,
        message: "Referrer code is required",
      });
    }

    // Find parent by referrerCode (refCode of the referrer)
    // Note: refCode in database is lowercase, convert referrerCode to lowercase for lookup
    const parent = await User.findOne({
      refCode: referrerCode.trim().toLowerCase(),
    });
    if (!parent) {
      return res.status(400).json({
        success: false,
        message: "Invalid referrer code. Please check your referral code.",
      });
    }

    // Check if parent has purchased a package
    const parentHasPackage = await Transaction.findOne({
      userId: parent._id,
      type: "payment",
      status: "completed",
    });
    if (!parentHasPackage) {
      return res.status(400).json({
        success: false,
        message:
          "This referrer has not purchased any package yet. You cannot register with this referral code.",
      });
    }

    const parentId = parent._id;
    const ancestors = [parent._id, ...(parent.ancestors || [])];

    // Generate verification token
    const verificationToken = crypto.randomBytes(32).toString("hex");

    // Create user (refCode will be auto-generated by pre-save hook)
    const user = new User({
      email,
      username,
      password,
      walletAddress: walletAddress.trim(),
      fullName: fullName?.trim() || "",
      phoneNumber: phoneNumber?.trim() || "",
      identityNumber: identityNumber?.trim() || "",
      parentId,
      ancestors,
      refCode: username,
      emailVerificationToken: verificationToken,
    });

    await user.save();

    // Update parent's direct referrals count
    await User.findByIdAndUpdate(parentId, {
      $inc: { directReferrals: 1 },
    });

    // Send verification email
    await sendVerificationEmail(email, verificationToken);

    res.status(201).json({
      success: true,
      message: "Registration successful. Please check your email to verify your account.",
      data: {
        userId: user._id,
        email: user.email,
        username: user.username,
        refCode: user.refCode, // Return the auto-generated refCode
      },
    });
  } catch (error) {
    console.error("Register error:", error);
    res.status(500).json({
      success: false,
      message: error.message || "Registration failed",
    });
  }
};

export const checkReferrerCode = async (req, res) => {
  try {
    const { referrerCode } = req.query;

    if (!referrerCode || !referrerCode.trim()) {
      return res.status(400).json({
        success: false,
        message: "Referrer code is required",
        isValid: false,
      });
    }

    // Find parent by referrerCode
    const parent = await User.findOne({
      refCode: referrerCode.trim().toLowerCase(),
    });

    if (!parent) {
      return res.json({
        success: true,
        isValid: false,
        message: "Invalid referrer code",
      });
    }

    // Check if parent has purchased a package
    const parentHasPackage = await Transaction.findOne({
      userId: parent._id,
      type: "payment",
      status: "completed",
    });

    if (!parentHasPackage) {
      return res.json({
        success: true,
        isValid: false,
        message:
          "This referrer has not purchased any package yet. You cannot register with this referral code.",
      });
    }

    return res.json({
      success: true,
      isValid: true,
      message: "Referrer code is valid",
    });
  } catch (error) {
    console.error("Check referrer code error:", error);
    res.status(500).json({
      success: false,
      isValid: false,
      message: "Failed to check referrer code",
    });
  }
};

export const verifyEmail = async (req, res) => {
  try {
    const { token } = req.query;

    if (!token) {
      return res.status(400).json({
        success: false,
        message: "Verification token is required",
      });
    }

    // Find user by token
    const user = await User.findOne({ emailVerificationToken: token });

    if (!user) {
      // Token not found - invalid or expired token
      return res.status(400).json({
        success: false,
        message: "Invalid or expired verification token",
        code: "INVALID_TOKEN",
      });
    }

    // Check if this user's email is already verified
    if (user.isEmailVerified) {
      // User has already verified, return success message but don't modify token
      return res.json({
        success: true,
        message: "Email has already been verified",
        alreadyVerified: true,
      });
    }

    // Verify the email (but keep the token)
    user.isEmailVerified = true;
    // Don't delete emailVerificationToken - keep it for future reference
    await user.save();

    res.json({
      success: true,
      message: "Email verified successfully",
      alreadyVerified: false,
    });
  } catch (error) {
    console.error("Email verification error:", error);
    res.status(500).json({
      success: false,
      message: "Email verification failed",
    });
  }
};

export const resendVerification = async (req, res) => {
  try {
    const { email } = req.body;

    const user = await User.findOne({ email });

    if (!user) {
      return res.status(404).json({
        success: false,
        message: "User not found",
      });
    }

    if (user.isEmailVerified) {
      return res.status(400).json({
        success: false,
        message: "Email already verified",
      });
    }

    const verificationToken = crypto.randomBytes(32).toString("hex");
    user.emailVerificationToken = verificationToken;
    await user.save();

    await sendVerificationEmail(email, verificationToken);

    res.json({
      success: true,
      message: "Verification email sent",
    });
  } catch (error) {
    console.error("Resend verification error:", error);
    res.status(500).json({
      success: false,
      message: "Failed to resend verification email",
    });
  }
};

export const login = async (req, res) => {
  try {
    const { email, password } = req.body;

    const user = await User.findOne({ email });

    if (!user) {
      return res.status(401).json({
        success: false,
        message: "Invalid credentials",
      });
    }

    if (!user.isEmailVerified) {
      return res.status(401).json({
        success: false,
        message: "Please verify your email first",
      });
    }

    if (!user.isActive) {
      return res.status(401).json({
        success: false,
        message: "Account is inactive",
      });
    }

    const isPasswordValid = await bcrypt.compare(password, user.password);

    if (!isPasswordValid) {
      return res.status(401).json({
        success: false,
        message: "Invalid credentials",
      });
    }

    // If 2FA is enabled, require 2FA verification
    if (user.isTwoFactorEnabled) {
      return res.status(200).json({
        success: true,
        requires2FA: true,
        message: "2FA verification required",
        tempToken: jwt.sign(
          { userId: user._id, type: "user", temp: true },
          process.env.JWT_SECRET,
          { expiresIn: "5m" }
        ),
      });
    }

    // Generate tokens
    const { accessToken, refreshToken } = generateTokens(user._id);

    res.json({
      success: true,
      message: "Login successful",
      data: {
        accessToken,
        refreshToken,
        user: {
          id: user._id,
          email: user.email,
          username: user.username,
          role: user.role,
          refCode: user.refCode,
          isTwoFactorEnabled: user.isTwoFactorEnabled,
        },
      },
    });
  } catch (error) {
    console.error("Login error:", error);
    res.status(500).json({
      success: false,
      message: "Login failed",
    });
  }
};

export const verify2FA = async (req, res) => {
  try {
    const { token, tempToken } = req.body;

    if (!tempToken) {
      return res.status(400).json({
        success: false,
        message: "Temporary token required",
      });
    }

    const decoded = jwt.verify(tempToken, process.env.JWT_SECRET);
    if (!decoded.temp || decoded.type !== "user") {
      return res.status(400).json({
        success: false,
        message: "Invalid temporary token",
      });
    }

    const user = await User.findById(decoded.userId);

    if (!user || !user.isTwoFactorEnabled) {
      return res.status(400).json({
        success: false,
        message: "2FA not enabled for this user",
      });
    }

    const isValid = verifyToken(user.twoFactorSecret, token);

    if (!isValid) {
      return res.status(401).json({
        success: false,
        message: "Invalid 2FA token",
      });
    }

    // Generate tokens
    const { accessToken, refreshToken } = generateTokens(user._id);

    res.json({
      success: true,
      message: "2FA verification successful",
      data: {
        accessToken,
        refreshToken,
        user: {
          id: user._id,
          email: user.email,
          username: user.username,
          role: user.role,
          refCode: user.refCode,
          isTwoFactorEnabled: user.isTwoFactorEnabled,
        },
      },
    });
  } catch (error) {
    console.error("2FA verification error:", error);
    res.status(500).json({
      success: false,
      message: "2FA verification failed",
    });
  }
};

export const setup2FA = async (req, res) => {
  try {
    const user = await User.findById(req.user._id);

    if (user.isTwoFactorEnabled) {
      return res.status(400).json({
        success: false,
        message: "2FA is already enabled",
      });
    }

    const secret = generateSecret(user.username);
    user.twoFactorSecret = secret.base32;
    await user.save();

    const qrCode = await generateQRCode(secret.otpauth_url);

    res.json({
      success: true,
      data: {
        secret: secret.base32,
        qrCode,
        manualEntryKey: secret.base32,
      },
    });
  } catch (error) {
    console.error("2FA setup error:", error);
    res.status(500).json({
      success: false,
      message: "2FA setup failed",
    });
  }
};

export const disable2FA = async (req, res) => {
  try {
    const { password, token } = req.body;

    const user = await User.findById(req.user._id);

    if (!user.isTwoFactorEnabled) {
      return res.status(400).json({
        success: false,
        message: "2FA is not enabled",
      });
    }

    // Verify password
    const isPasswordValid = await bcrypt.compare(password, user.password);
    if (!isPasswordValid) {
      return res.status(401).json({
        success: false,
        message: "Invalid password",
      });
    }

    // Verify 2FA token
    const isValid = verifyToken(user.twoFactorSecret, token);
    if (!isValid) {
      return res.status(401).json({
        success: false,
        message: "Invalid 2FA token",
      });
    }

    user.isTwoFactorEnabled = false;
    user.twoFactorSecret = null;
    await user.save();

    res.json({
      success: true,
      message: "2FA disabled successfully",
    });
  } catch (error) {
    console.error("2FA disable error:", error);
    res.status(500).json({
      success: false,
      message: "Failed to disable 2FA",
    });
  }
};

export const refreshToken = async (req, res) => {
  try {
    const { refreshToken } = req.body;

    if (!refreshToken) {
      return res.status(400).json({
        success: false,
        message: "Refresh token required",
      });
    }

    const decoded = jwt.verify(refreshToken, process.env.JWT_REFRESH_SECRET);
    if (decoded.type !== "user") {
      return res.status(401).json({
        success: false,
        message: "Invalid refresh token",
      });
    }

    const { accessToken } = generateTokens(decoded.userId);

    res.json({
      success: true,
      data: { accessToken },
    });
  } catch (error) {
    res.status(401).json({
      success: false,
      message: "Invalid refresh token",
    });
  }
};

